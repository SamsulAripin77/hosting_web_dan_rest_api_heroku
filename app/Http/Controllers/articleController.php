    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $article = article::find($id);
        return view('blogs.edit',['article'=>$article]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $article = article::find($id);
        $request->validate([
            'author' => 'required',
            'title' => 'required|min:15',
            'paragraph' => 'required|min:30'
        ]);

        $article->update($request->all());

        return redirect()->route('blogs.edit',$article->id)->with('status','berhasil merubah article');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $article = article::find($id);
        $article->delete();
        return redirect()->route('blogs.index')->with('status','berhasil menghapus article');
    }
